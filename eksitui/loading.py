screens = [kite, bridge]


def output():
    screen = random.choice(screens)
    row = len(screen.split("\n")) - 2
    a = int(255 / row)
    spl = screen.splitlines()
    text = ""
    for i in range(row):
        r, g, b = 40, 255 - (i * a), 120
        text = (
            text
            + ("\n" if i != 0 else "")
            + f"[rgb({r},{g},{b})]"
            + spl[i + 1]
            + " [/]"
        )
        # logotext.append(spl[i + 1] + "\n", style=f"rgb({r},{g},{b})")
    return text
